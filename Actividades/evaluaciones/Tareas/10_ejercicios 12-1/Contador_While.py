#Solicite un número entero al usuario y utilice un ciclo while para contar desde 1 hasta ese número, mostrando cada valor.

numero_max = int(input("¿Hasta que numero deseas contar?, ingrese un numero: "))
contador = 0
while contador < numero_max:
    contador +=1
    print(contador)
    
#fue facil de hacer con el while simplemente realice el input lo meto con el while y que vaya sumando uno con el contador y luego se imprime 
