class Producto:
    def __init__(self, id, nombre, categoria, precio, stock):
        self.id = id
        self.nombre = nombre
        self.categoria = categoria
        self.precio = precio
        self.stock = stock

# GETS
def getId(self):
	return self.id

def getNombre(self):
	return self.nombre

def getCategoria(self):
	return self.categoria

def getPrecio(self):
	return self.precio

def getStock(self):
	return self.stock

# SETS
def setId(self, id):
	self.id = id

def setNombre(self, nombre):
	self.nombre = nombre

def setCategoria(self, categoria):
	self.categoria = categoria

def setPrecio(self, precio):
	self.precio = precio
  
def setStock(self, stock):
	self.stock = stock
